    /*
     * 获取行政区级别，若为省级则为1，市级则为2，县级则为3，其他为4
     */
    public static int GetRegionLevel(String regionCode){
        regionCode = GetStandardRegionCode(regionCode);
        int length = regionCode.length();
        int offset = 0;
        while(regionCode.charAt(length-offset-1) == '0'){
            offset++;
        }
        int codelength = length-offset;
        if(codelength>6){
            return 4;
        }else if(codelength==6||codelength==5){
            return 3;
        }else if(codelength==3||codelength==4){
            return 2;
        }else if(codelength<=2){
            return 1;
        }
        return 0;
    }

